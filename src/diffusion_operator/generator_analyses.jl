# =====================================================================
# Analyses on a DiffusionGenerator. Each function below is a
# self-contained problem on the generator ‚Äî it takes a generator plus a
# problem spec (sets, target, observable, ‚Ä¶) and returns the answer.
# These are the building blocks consumed by bundled analyses like
# [`ReactiveTransition`](@ref).
#
# Future extensions (spectrum, autocorrelation, semigroup propagation,
# ‚Ä¶) plug in here without touching the discretisation or higher-level
# bundles.
# =====================================================================

# ---------------------------------------------------------------------
# A / B mask normalisation (shared by every Layer-3 analysis that takes
# subsets of the state space).
# ---------------------------------------------------------------------

# `Bool <: Integer`, so `AbstractVector{Bool}` is more specific than
# `AbstractVector{<:Integer}`; Julia's method dispatch picks the right one.
function _to_mask(spec::AbstractVector{Bool}, grid::CartesianGrid)
    N = ncells(grid)
    length(spec) == N ||
        throw(DimensionMismatch("mask length $(length(spec)) ‚âÝ ncells = $N"))
    mask = falses(N)
    @inbounds for i in 1:N
        mask[i] = spec[i]
    end
    return mask
end

function _to_mask(spec::AbstractVector{<:Integer}, grid::CartesianGrid)
    N = ncells(grid)
    mask = falses(N)
    for i in spec
        1 <= i <= N || throw(BoundsError("cell index $i out of range 1:$N"))
        mask[i] = true
    end
    return mask
end

function _to_mask(spec, grid::CartesianGrid{D}) where {D}
    N = ncells(grid)
    mask = falses(N)
    LI = LinearIndices(grid.nbox)
    @inbounds for I in CartesianIndices(grid.nbox)
        if spec(cell_center(grid, I))::Bool
            mask[LI[I]] = true
        end
    end
    return mask
end

_to_mask(spec::BitVector, grid::CartesianGrid) = begin
    N = ncells(grid)
    length(spec) == N ||
        throw(DimensionMismatch("mask length $(length(spec)) ‚âÝ ncells = $N"))
    return copy(spec)
end

# ---------------------------------------------------------------------
# Stationary distribution: solve Q·µÄ œÅ = 0 with ‚à´ œÅ dV = 1.
# ---------------------------------------------------------------------

"""
$(TYPEDSIGNATURES)

Invariant probability density of the discretised process generated by
`gen`: solves the discrete adjoint stationary equation `Q·µÄ œÅ = 0`
augmented with the normalisation `‚à´ œÅ dV = 1`, where `Q = gen.Q` is the
rate matrix.

Returns a length-`ncells(gen)` vector with `dot(œÅ, fill(cell_volume(grid), N)) = 1`.

Throws `ArgumentError` if any axis uses absorbing boundary conditions ‚Äî
the resulting chain has mass loss and admits no nontrivial normalised
invariant.

`alg = nothing` (default) uses sparse direct LU. Pass a LinearSolve.jl
algorithm (e.g. `KrylovJL_GMRES()`) to use an iterative solver.
"""
function stationary_distribution(
    gen::DiffusionGenerator; alg=nothing
)::Vector{Float64}
    any(b -> b isa Absorbing, gen.bc) && throw(
        ArgumentError(
            "stationary_distribution is undefined for absorbing boundary " *
            "conditions: the chain leaks probability through the boundary " *
            "and has no nontrivial normalised invariant.",
        ),
    )
    weights = fill(cell_volume(gen.grid), ncells(gen))
    return _invariant_density(gen.Q, weights; alg=alg)
end

# ---------------------------------------------------------------------
# Committors: solve Qq = 0 with Dirichlet BCs.
# ---------------------------------------------------------------------

"""
$(TYPEDSIGNATURES)

Forward committor `q‚Å∫[i]`: probability that a trajectory of the process
generated by `gen` started at cell `i` reaches `B` before `A`. Solves the
homogeneous linear system `Q q‚Å∫ = 0` with Dirichlet conditions `q‚Å∫ = 0`
on `A` and `q‚Å∫ = 1` on `B`.

`A` and `B` accept a predicate `x -> Bool`, a `BitVector` of length
`ncells(gen)`, or a vector of linear cell indices.

`alg = nothing` (default) uses sparse direct LU. Pass a LinearSolve.jl
algorithm (e.g. `KrylovJL_GMRES()`) to use an iterative solver.
"""
function forward_committor(
    gen::DiffusionGenerator, A, B; alg=nothing
)::Vector{Float64}
    grid = gen.grid
    A_mask = _to_mask(A, grid)
    B_mask = _to_mask(B, grid)
    return _forward_committor(gen.Q, A_mask, B_mask; alg=alg)
end

function _forward_committor(
    Q::SparseMatrixCSC{Float64,Int}, A_mask::BitVector, B_mask::BitVector;
    alg=nothing,
)::Vector{Float64}
    any(A_mask) || throw(ArgumentError("set A is empty"))
    any(B_mask) || throw(ArgumentError("set B is empty"))
    any(A_mask .& B_mask) && throw(ArgumentError("sets A and B overlap"))
    N = size(Q, 1)
    mask_bc = A_mask .| B_mask
    bc_values = zeros(Float64, N)
    @inbounds for n in eachindex(B_mask)
        B_mask[n] && (bc_values[n] = 1.0)
    end
    return _solve_dirichlet(Q, mask_bc, bc_values; alg=alg)
end

"""
$(TYPEDSIGNATURES)

Backward committor `q‚Åª[i]`: probability that a trajectory in cell `i` last
visited `A` (rather than `B`). For reversible processes `q‚Åª = 1 - q‚Å∫`.

By default `q‚Åª` is solved on the discrete adjoint of `gen.Q` (time-
reversal w.r.t. the invariant measure ‚Äî this requires solving the
stationary distribution as well). Pass `reverse::DiffusionGenerator` to
instead solve on a separately-supplied physical reverse-drift generator;
useful as a cross-check or for non-reversible systems where the exact
reverse SDE is known.

`alg = nothing` (default) uses sparse direct LU. Pass a LinearSolve.jl
algorithm (e.g. `KrylovJL_GMRES()`) to use an iterative solver.
"""
function backward_committor(
    gen::DiffusionGenerator, A, B;
    reverse::Union{Nothing,DiffusionGenerator}=nothing, alg=nothing,
)::Vector{Float64}
    grid = gen.grid
    A_mask = _to_mask(A, grid)
    B_mask = _to_mask(B, grid)
    if reverse === nothing
        œÅ = stationary_distribution(gen; alg=alg)
        return _backward_committor_adjoint(gen.Q, œÅ, A_mask, B_mask; alg=alg)
    else
        return _backward_committor_explicit(reverse.Q, A_mask, B_mask; alg=alg)
    end
end

function _backward_committor_adjoint(
    Q::SparseMatrixCSC{Float64,Int},
    œÅ::Vector{Float64},
    A_mask::BitVector,
    B_mask::BitVector;
    alg=nothing,
)::Vector{Float64}
    any(A_mask) || throw(ArgumentError("set A is empty"))
    any(B_mask) || throw(ArgumentError("set B is empty"))
    any(A_mask .& B_mask) && throw(ArgumentError("sets A and B overlap"))
    Qadj = _adjoint_generator(Q, œÅ)
    return _backward_committor_explicit(Qadj, A_mask, B_mask; alg=alg)
end

function _backward_committor_explicit(
    Qrev::SparseMatrixCSC{Float64,Int}, A_mask::BitVector, B_mask::BitVector;
    alg=nothing,
)::Vector{Float64}
    N = size(Qrev, 1)
    mask_bc = A_mask .| B_mask
    bc_values = zeros(Float64, N)
    @inbounds for n in eachindex(A_mask)
        A_mask[n] && (bc_values[n] = 1.0)
    end
    return _solve_dirichlet(Qrev, mask_bc, bc_values; alg=alg)
end

# ---------------------------------------------------------------------
# Mean first-passage time: solve Q œÑ = -1 with œÑ = 0 on the target.
# ---------------------------------------------------------------------

"""
$(TYPEDSIGNATURES)

Mean first-passage time `œÑ[i]` from cell `i` to the target set: the
expected time for a trajectory of the process generated by `gen` to first
hit `target` starting at `i`. Solves `Q œÑ = -1` on the free cells with
Dirichlet condition `œÑ = 0` on `target`.

`target` accepts a predicate, a `BitVector`, or a vector of linear cell
indices.

`alg = nothing` (default) uses sparse direct LU. Pass a LinearSolve.jl
algorithm (e.g. `KrylovJL_GMRES()`) to use an iterative solver.
"""
function mean_first_passage_time(
    gen::DiffusionGenerator, target; alg=nothing
)::Vector{Float64}
    target_mask = _to_mask(target, gen.grid)
    any(target_mask) || throw(ArgumentError("target set is empty"))
    Q = gen.Q
    N = size(Q, 1)
    bc_values = zeros(Float64, N)
    source = fill(-1.0, N)
    return _solve_dirichlet(Q, target_mask, bc_values; source=source, alg=alg)
end

"""
$(TYPEDSIGNATURES)

Variance `Var[œÑ | X_0 = i] = E[œÑ¬≤] ‚àí E[œÑ]¬≤` of the first-passage time
to `target`, as a function of starting cell `i`.

The first moment `œÑ_1 = E[œÑ]` solves `Q œÑ_1 = -1` with `œÑ_1|_target = 0`
([`mean_first_passage_time`](@ref)); the second moment `œÑ_2 = E[œÑ¬≤]`
solves `Q œÑ_2 = -2 œÑ_1` with `œÑ_2|_target = 0`. The variance follows
from `Var[œÑ] = œÑ_2 ‚àí œÑ_1¬≤`.

Returns a length-`ncells(gen)` vector of variances, all `‚â• 0`.

`alg = nothing` (default) uses sparse direct LU. Pass a LinearSolve.jl
algorithm to use an iterative solver.
"""
function first_passage_variance(
    gen::DiffusionGenerator, target; alg=nothing
)::Vector{Float64}
    target_mask = _to_mask(target, gen.grid)
    any(target_mask) || throw(ArgumentError("target set is empty"))
    Q = gen.Q
    N = size(Q, 1)
    bc_values = zeros(Float64, N)

    œÑ_1 = _solve_dirichlet(Q, target_mask, bc_values; source=fill(-1.0, N), alg=alg)
    œÑ_2 = _solve_dirichlet(Q, target_mask, bc_values; source=-2 .* œÑ_1, alg=alg)
    return œÑ_2 .- œÑ_1 .^ 2
end
