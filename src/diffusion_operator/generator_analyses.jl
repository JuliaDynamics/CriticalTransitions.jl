"""
$(TYPEDSIGNATURES)

Mean first-passage time `τ[i]` from cell `i` to the target set: the
expected time for a trajectory of the process generated by `generator` to first
hit `target` starting at `i`. Solves `Q τ = -1` on the free cells with
Dirichlet condition `τ = 0` on `target`.

`target` accepts a predicate, a `BitVector`, or a vector of linear cell
indices.

Default `alg` is `UMFPACKFactorization()` (sparse direct LU). Pass any
`SciMLLinearSolveAlgorithm` for an iterative solver.

See also [`first_passage_variance`](@ref) and [`forward_committor`](@ref).
"""
function mean_first_passage_time(
        generator::DiffusionGenerator, target;
        alg = UMFPACKFactorization(),
        kwargs...
    )::Vector{Float64}
    target_mask, bc_values, source = _mfpt_system(generator, target)
    return _solve_dirichlet(
        generator.Q, target_mask, bc_values, alg;
        source = source, kwargs...
    )
end


function _mfpt_system(generator::DiffusionGenerator, target)
    target_mask = _to_mask(target, generator.grid)
    any(target_mask) || throw(ArgumentError("target set is empty"))
    N = size(generator.Q, 1)
    return target_mask, zeros(Float64, N), fill(-1.0, N)
end

"""
$(TYPEDSIGNATURES)

Variance `Var[τ | X_0 = i] = E[τ²] − E[τ]²` of the first-passage time
to `target`, as a function of starting cell `i`.

The first moment `τ_1 = E[τ]` solves `Q τ_1 = -1` with `τ_1|_target = 0`
([`mean_first_passage_time`](@ref)); the second moment `τ_2 = E[τ²]`
solves `Q τ_2 = -2 τ_1` with `τ_2|_target = 0`. The variance follows
from `Var[τ] = τ_2 − τ_1²`.

Returns a length-`ncells(generator)` vector of variances, all `≥ 0`.

Default `alg` is `UMFPACKFactorization()` (sparse direct LU). Pass any
`SciMLLinearSolveAlgorithm` for an iterative solver.

See also [`mean_first_passage_time`](@ref).
"""
function first_passage_variance(
        generator::DiffusionGenerator, target;
        alg = UMFPACKFactorization(),
        kwargs...
    )::Vector{Float64}
    target_mask, bc_values, source = _mfpt_system(generator, target)
    τ_1 = _solve_dirichlet(
        generator.Q, target_mask, bc_values, alg;
        source = source, kwargs...
    )
    τ_2 = _solve_dirichlet(
        generator.Q, target_mask, bc_values, alg;
        source = -2 .* τ_1, kwargs...
    )
    return τ_2 .- τ_1 .^ 2
end
